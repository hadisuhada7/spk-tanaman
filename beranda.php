			<p align="justify"> 
				&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  
				Sistem Pendukung Keputusan metode <b> Promethee </b><i> (Preference Ranking Organization Method for 
				Enrichment Evaluation) </i> digunakan untuk menentukan keputusan yang paling optimal dari beberapa 
				alternatif keputusan yang telah dirumuskan sebelumnya. Dimana semua data digabung menjadi satu dengan 
				bobot penilaian yang telah diperoleh melalui penilaian atau survey. Promethee merupakan salah satu 
				metode yang termasuk dalam MCDM <i> (Multi Criteria Decision Making). </i><b> (Pratama, 2014) </b>
			</p>

			<p align="left">
				<b> Petunjuk Penggunaan : </b> <br>
				1) Pertama pilih Menu Kriteria, kemudian input Data Kriteria dengan benar. <br>
				2) Selajutnya pilih Menu Jenis Tanaman, kemudian input Data Jenis Tanaman dengan benar. <br>
				3) Berikutnya pilih Menu Karakteristik Lahan, kemudian input Data Menu Karakteristik Lahan dengan benar. <br>
				4) Kemudian pilih Menu Nilai Alternatif, selanjutnya input Nilai dari masing-masing Alternatif dengan benar. <br>
				5) Terkahir pilih Menu Perhitungan untuk melihat hasil perhitungan Jenis Tanaman terbaik.
			</p>

